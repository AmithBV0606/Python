# Typecasting in python

# Converting from string to an integer
a = '10'
sum = int(a) + 20
print(sum)

# Converting from number to string
a = 20
total = str(a) + "Amith"
print(total)

# Converting to float
x = float(1)     # x will be 1.0
y = float(2.8)   # y will be 2.8
z = float("3")   # z will be 3.0
w = float("4.2") # w will be 4.2